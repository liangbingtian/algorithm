       `ods`.`a`.`username`,
       `ods`.`a`.`date_str`,
       `ods`.`a`.`trans_days`,
       `ods`.`a`.`caliber`,
       `ods`.`a`.`order_status`,
       `ods`.`a`.`gift_flag`,
       `ods`.`a`.`effect`,
       `ods`.`a`.`is_daily`,
       `ods`.`a`.`start_date`,
       `ods`.`a`.`end_date`,
       `ods`.`a`.`period`,
       `ods`.`a`.`pin`,
       `ods`.`a`.`business`,
       `ods`.`a`.`campaign_type`,
       `ods`.`a`.`campaign_id`,
       `ods`.`a`.`campaign_name`,
       `ods`.`a`.`group_id`,
       `ods`.`a`.`group_name`,
       `ods`.`a`.`kw_id`,
       `ods`.`a`.`keyword`,
