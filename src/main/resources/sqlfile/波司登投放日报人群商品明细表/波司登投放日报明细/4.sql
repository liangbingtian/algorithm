FROM `ods`.`jd_custom_report_keyword` AS `a`
         INNER JOIN `jd_digital_marketing`.`jd_custom_report_batch` AS `b` ON ((((((
                                                                                       `ods`.`a`.`username` = `jd_digital_marketing`.`b`.`username`
                                                                                       )
    AND (`ods`.`a`.`date_str` = `jd_digital_marketing`.`b`.`date_str`))
    AND (`ods`.`a`.`trans_days` = `jd_digital_marketing`.`b`.`trans_days`))
    AND (`ods`.`a`.`caliber` = `jd_digital_marketing`.`b`.`caliber`))
    AND (`ods`.`a`.`order_status` = `jd_digital_marketing`.`b`.`order_status`))
    AND (`ods`.`a`.`batch` = `jd_digital_marketing`.`b`.`batch`))
    AND (`jd_digital_marketing`.`b`.`template_key` = 'jd_custom_report_keyword');